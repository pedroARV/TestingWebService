package edu.pedro.examenrest.model;

public class Alumno {

	private String nombre;
	private String apellidos;
	private String calificaciones;
	private String sexo;
	private String beca;
	private String semestre;
	private String almacenar;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellidos() {
		return apellidos;
	}

	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}

	public String getCalificaciones() {
		return calificaciones;
	}

	public void setCalificaciones(String calificaciones) {
		this.calificaciones = calificaciones;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public String getBeca() {
		return beca;
	}

	public void setBeca(String beca) {
		this.beca = beca;
	}

	public String getSemestre() {
		return semestre;
	}

	public void setSemestre(String semestre) {
		this.semestre = semestre;
	}

	public String getAlmacenar() {
		return almacenar;
	}

	public void setAlmacenar(String almacenar) {
		this.almacenar = almacenar;
	}

}
