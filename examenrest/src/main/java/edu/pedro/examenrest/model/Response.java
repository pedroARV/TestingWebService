package edu.pedro.examenrest.model;

public class Response {

	private String matriclua;
	private String nombre;
	private int calificacion;
	private String sexo;
	private String beca;
	private String status_del_alumno;
	private String semestre;
	private String almacenar;
	private String ruta;

	public String getMatriclua() {
		return matriclua;
	}

	public void setMatriclua(String matriclua) {
		this.matriclua = matriclua;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getCalificacion() {
		return calificacion;
	}

	public void setCalificacion(int calificacion) {
		this.calificacion = calificacion;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public String getBeca() {
		return beca;
	}

	public void setBeca(String beca) {
		this.beca = beca;
	}

	public String getStatus_del_alumno() {
		return status_del_alumno;
	}

	public void setStatus_del_alumno(String status_del_alumno) {
		this.status_del_alumno = status_del_alumno;
	}

	public String getSemestre() {
		return semestre;
	}

	public void setSemestre(String semestre) {
		this.semestre = semestre;
	}

	public String getAlmacenar() {
		return almacenar;
	}

	public void setAlmacenar(String almacenar) {
		this.almacenar = almacenar;
	}

	public String getRuta() {
		return ruta;
	}

	public void setRuta(String ruta) {
		this.ruta = ruta;
	}

}
